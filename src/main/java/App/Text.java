public class Text {
    private String opening;
    private String body;
    private String closing;

    public void setOpening(String input){
        opening = input;
    }

    public String getOpening(){
        return opening;
    }

    public void setBody(String input){
        body = input;
    }

    public String getBody(){
        return body;
    }

    public void setClosing(String input){
        closing = input;
    }

    public String getClosing(){
        return closing;
    }




}
